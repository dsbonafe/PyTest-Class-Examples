class game:
    def __init__(self):
        pass

    def play(number):
        answer = number
        if number % 3 == 0:
            answer = 'chease'
        elif number % 5 == 0:
            answer = 'goiabada'
        if number % 15 == 0:
            answer = 'Romeo S2 Juliet'
        return answer